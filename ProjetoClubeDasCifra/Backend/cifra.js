class cifra{
    constructor(musica, instrumento, afinacao){
        this.musica = musica
        this.instrumento = instrumento
        this.afinacao = afinacao
    }
    getNomedaCifra() {
        return musica.nome
    }
    getAfinacao(){
        return afinacao
    }
    getInstrumento(){
        return instrumento
    }
}